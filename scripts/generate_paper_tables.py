    hr_p4 = next(e["pooled_mean_cosine_distance"] for e in hr_bd if e["scale"] == 4.0)
    hr_pct = (1 - hr_p4 / hr_p0) * 100
    hr_me = hr_stats["mixed_effects"]
    hr_within = f"None (beta ≈ 0, p = {fmt_f(hr_me['p_value'], 2)})"

    # style_full
    sf_m = load_json(OUTPUTS / "style_full" / "metrics.json")
    sf_stats = load_json(OUTPUTS / "style_full" / "stats.json")
    sf_p0 = next(m["mean_pairwise_cosine_distance"] for m in sf_m if m["scale"] == 0.0)
    sf_p4 = next(m["mean_pairwise_cosine_distance"] for m in sf_m if m["scale"] == 4.0)
    sf_pct = (1 - sf_p4 / sf_p0) * 100
    sf_me = sf_stats["mixed_effects"]
    sf_within = f"Mild (beta = {fmt_f(sf_me['beta'], 3)}, p = {fmt_f(sf_me['p_value'], 3)})"

    # creativity_full
    cf_m = load_json(OUTPUTS / "creativity_full" / "metrics.json")
