def gen_passk_preliminary() -> str:
    """Section 4.6 — preliminary pass@k (n=10, unsteered vs steered)."""
    unst_path = OUTPUTS / "test_unsteered_full" / "humaneval" / "pass_at_k.json"
    strd_curves = load_json(OUTPUTS / "passk_test_steered" / "code" / "pass_at_k_curves.json")
    strd = strd_curves[0]

    if not unst_path.exists():
        print("  SKIP: outputs/test_unsteered_full/humaneval/pass_at_k.json not found")
        print("  Run: uv run scripts/eval/extract_preliminary_passk.py")
        return ""
    unst = load_json(unst_path)

    lines = [
        "| Metric | Unsteered (α=0) | Steered (α=2) | Δ | 95% CI | Paired t | p |",
        "|--------|:-:|:-:|:-:|:-:|:-:|:-:|",
    ]
    for variant in ["base", "plus"]:
        unst_k = unst[f"pass_at_k_{variant}"]
        strd_k = strd[f"pass_at_k_{variant}"]
        for k_str in sorted(unst_k.keys(), key=int):
            if k_str not in strd_k:
                continue
            u = unst_k[k_str]
            s = strd_k[k_str]
            delta = s - u

            # Compute paired t-test from per-problem data
            from src.pass_at_k import pass_at_k as pak
            import scipy.stats as st
            n_samp = unst["n_samples_per_problem"]
            k_int = int(k_str)
            status_key = f"per_problem_{variant}"
            u_scores = np.array([pak(p["n"], p["c"], k_int) for p in unst[status_key]])
            s_scores = np.array([pak(p["n"], p["c"], k_int) for p in strd[f"per_problem_{variant}"]])
            diffs = s_scores - u_scores
            se = float(np.std(diffs, ddof=1) / np.sqrt(len(diffs)))
            ci_95 = 1.96 * se
            t_stat, p_val = st.ttest_rel(s_scores, u_scores)

            p_str = f"**{fmt_f(p_val, 3)}**" if p_val < 0.05 else fmt_f(p_val, 3)
            label = f"**pass@{k_str} ({variant})**"
            lines.append(
                f"| {label} | {fmt_f(u, 3)} | {fmt_f(s, 3)} | {fmt_f(delta, 3)} "
                f"| ±{fmt_f(ci_95, 3)} | {fmt_f(t_stat, 2)} | {p_str} |"
            )
    table = "\n".join(lines)
