def _count_field(results: list[dict], field: str) -> dict[str, int]:
    """Count occurrences of Yes/No/Maybe/ERROR for a field in results."""
    counts: dict[str, int] = {"Yes": 0, "No": 0, "Maybe": 0}
    for r in results:
        val = r.get(field, "ERROR")
        if val in counts:
            counts[val] += 1
    return counts


