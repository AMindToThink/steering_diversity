def gen_experiment_stats(name: str) -> str:
    """Sections 4.2/4.3 — statistical tests for an experiment."""
    stats = load_json(OUTPUTS / name / "stats.json")
    me = stats["mixed_effects"]
    sp = stats["spearman_effect_size"]
    hb = stats["holm_bonferroni"]

    me_sig = "Yes" if me["p_value"] < 0.05 else "No"
    sp_sig = "Yes" if sp["p_value"] < 0.05 else "No"

    lines = [
        "| Test | Statistic | p-value | Significant? |",
        "| --- | --- | --- | :---: |",
        f"| Mixed-effects (primary) | beta = {fmt_f(me['beta'], 3)} | {fmt_p(me['p_value'])} | {me_sig} |",
        f"| Spearman rho | rho = {fmt_f(sp['rho'], 3)} | {fmt_p(sp['p_value'])} | {sp_sig} |",
    ]
    for metric_name, result in sorted(hb.items(), key=lambda kv: kv[1]["raw_p"]):
        if result["significant"]:
            lines.append(
                f"| Page's L ({metric_name}, Holm-corrected) | — | {fmt_p(result['adjusted_p'])} | Yes |"
            )
    table = "\n".join(lines)
    write_table(f"{name}_stats", table, f"outputs/{name}/stats.json")
    return table


def gen_cross_experiment() -> str:
    """Section 4.4 — cross-experiment comparison table."""
    # happy_recon
    hr_bd = load_json(OUTPUTS / "happy_recon" / "cosine_distance_breakdown.json")
    hr_stats = load_json(OUTPUTS / "happy_recon" / "stats.json")
