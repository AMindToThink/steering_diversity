def gen_happy_full_stats() -> str:
    """Section 4.1.1 — happy_full statistical tests."""
    stats = load_json(OUTPUTS / "happy_full" / "stats.json")
    me = stats["mixed_effects"]
    sp = stats["spearman_effect_size"]
    hb = stats["holm_bonferroni"]

    best_metric = min(hb, key=lambda k: hb[k]["raw_p"])
    best_raw = hb[best_metric]["raw_p"]
    best_adj = hb[best_metric]["adjusted_p"]

    table = (
        "| Test | Statistic | p-value | Significant? |\n"
        "| --- | --- | --- | :---: |\n"
        f"| Mixed-effects model (primary) | beta = {fmt_f(me['beta'], 4)} | {fmt_p(me['p_value'])} | No |\n"
        f"| Spearman rho (effect size) | rho = {fmt_f(sp['rho'], 3)} | {fmt_p(sp['p_value'])} | No |\n"
        f"| Page's L (all 6 metrics, Holm-Bonferroni) | best raw p = {fmt_f(best_raw, 3)} ({best_metric}) | adj p = {fmt_f(best_adj, 3)} | No |"
    )

    write_table("happy_full_stats", table, "outputs/happy_full/stats.json")
    return table


