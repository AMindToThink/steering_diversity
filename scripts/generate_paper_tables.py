# ---------------------------------------------------------------------------
# Paper update: replace tables between markers
# ---------------------------------------------------------------------------

def update_paper(tables: dict[str, str]) -> None:
    """Replace tables in the paper between <!-- BEGIN/END GENERATED TABLE --> markers."""
    text = PAPER_PATH.read_text()
    replacements = 0

    for name, content in tables.items():
        pattern = (
            rf"(<!-- BEGIN GENERATED TABLE: {re.escape(name)} -->)\n"
            r".*?"
            rf"\n(<!-- END GENERATED TABLE: {re.escape(name)} -->)"
        )
        replacement = rf"\1\n{content}\n\2"
        new_text, count = re.subn(pattern, replacement, text, flags=re.DOTALL)
        if count > 0:
            text = new_text
            replacements += count
            print(f"  Updated table '{name}' in paper")

    if replacements > 0:
        PAPER_PATH.write_text(text)
        print(f"\n  Updated {replacements} table(s) in {PAPER_PATH.relative_to(ROOT)}")
    else:
        print(
            "\n  No markers found in paper. Add markers like:\n"
            "  <!-- BEGIN GENERATED TABLE: table_name -->\n"
            "  ... table content ...\n"
            "  <!-- END GENERATED TABLE: table_name -->"
        )


