def fmt_p(p: float) -> str:
    """Format p-value for display."""
    if p < 0.001:
        return "< 0.001"
    return f"{p:.3f}"


def fmt_f(v: float, decimals: int = 3) -> str:
    """Format float with fixed decimals."""
    return f"{v:.{decimals}f}"


def load_json(path: Path) -> Any:
    with open(path) as f:
        return json.load(f)


def write_table(name: str, content: str, data_source: str) -> Path:
    """Write a generated table file with provenance header. Returns the path."""
    TABLES_DIR.mkdir(parents=True, exist_ok=True)
    header = (
        f"<!-- Generated by: scripts/generate_paper_tables.py -->\n"
        f"<!-- Data source: {data_source} -->\n\n"
    )
    path = TABLES_DIR / f"{name}.md"
    path.write_text(header + content + "\n")
    print(f"  Wrote {path.relative_to(ROOT)}")
    return path


def compute_within_prompt_cosine(embeddings_path: Path) -> list[dict[str, float]]:
    """Compute within-prompt mean pairwise cosine distance per scale from embeddings."""
    from sklearn.metrics.pairwise import cosine_distances

    data = np.load(embeddings_path)
    embeddings = data["embeddings"]
    scales = data["scales"]
    prompt_indices = data["prompt_indices"]

    unique_scales = sorted(set(float(s) for s in scales))
    unique_prompts = sorted(set(int(p) for p in prompt_indices))

    results: list[dict[str, float]] = []
    for scale in unique_scales:
        prompt_dists: list[float] = []
        for prompt_idx in unique_prompts:
            mask = (prompt_indices == prompt_idx) & np.isclose(scales, scale)
            group_embs = embeddings[mask]
            if group_embs.shape[0] > 1:
                cd = cosine_distances(group_embs)
                triu = np.triu_indices(group_embs.shape[0], k=1)
                prompt_dists.append(float(np.mean(cd[triu])))
        arr = np.array(prompt_dists)
        results.append({
            "scale": scale,
            "within_prompt_mean": float(arr.mean()),
            "within_prompt_se": float(arr.std(ddof=1) / np.sqrt(len(arr))),
        })
    return results


